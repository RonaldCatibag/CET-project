        function moveScroller() {
            var $anchor = $("#scroller-anchor");
            var $scroller = $('#scroller');

            var move = function() {
                var st = $(window).scrollTop();
                var ot = $anchor.offset().top;
                if (st > ot) {
                    $scroller.css({
                        position: "fixed",
                        top: "0px"
                    });
                } else {
                    $scroller.css({
                        position: "relative",
                        top: ""
                    });
                }
            };
            $(window).scroll(move);
            move();
        }

        $(function() {
            moveScroller();
        });
